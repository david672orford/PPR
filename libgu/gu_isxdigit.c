/** Determine if a character is an ASCII hexadecimal digit
 *
** This function replaces isdigit() from the C library.  The one in the C library 
** determines if c is a digit in the current locale.  We want to know if it is a
** digit in ASCII.
*/
int gu_ascii_isxdigit(int c)
	{
	switch(c)
		{
		case '0':
		case '1':
		case '2':
		case '3':
		case '4':
		case '5':
		case '6':
		case '7':
		case '8':
		case '9':
		case 'a':
		case 'b':
		case 'c':
		case 'd':
		case 'e':
		case 'f':
		case 'A':
		case 'B':
		case 'C':
		case 'D':
		case 'E':
		case 'F':
			return 1;
		default:
			return 0;
		}
	}

/** Returns the value of an ASCII hex digit
 *
 * This function returns the numberic value of an ASCII hex digit.  If the
 * character is not an ASCII hex digit, it returns -1.
 */
int gu_ascii_xdigit_value(int c)
	{
	switch(c)
		{
		case '0':
		case '1':
		case '2':
		case '3':
		case '4':
		case '5':
		case '6':
		case '7':
		case '8':
		case '9':
			return c - '0';
		case 'a':
		case 'b':
		case 'c':
		case 'd':
		case 'e':
		case 'f':
			return c - 'a' + 10;
		case 'A':
		case 'B':
		case 'C':
		case 'D':
		case 'E':
		case 'F':
			return c - 'A' + 10;
		default:
			return -1;
		}
	}

